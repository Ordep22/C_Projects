#include <stdio.h>
#include <stdlib.h>

int main(){

    printf("-------------------------\n");
    printf("\t\t\t\t\t\tOla Devs\n");
    printf("\t\t\t\t\tAula 14: Escape Sequence\n");
    printf("\t\t\t\t\t\tLet`s Code\n");
    printf("-------------------------\n");

    /*
        Os caracteres de escape são:

        1) \n -> quebramos uma linha 
        2) \t -> dar um tab no momento em que estamos imprimindo 
        3) \\ -> para imprimir uma barra invertida
        4) \" -> para imprimir uma aspas dupla
        5) \' -> para imprimir uma aspa simples 
        6) \0 -> indicador de finalização de uma instrução


    */




    return 0;

}