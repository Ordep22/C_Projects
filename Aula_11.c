#include <stdio.h>
#include <stdlib.h>

int main()
{

    printf("\n");
    printf("Aula 11: Operadores metemáticos\n");
    printf("\n");

    int num1, num2, soma, sub, prod, div;

    num1 = 10;
    num2 = 6;

    soma = num1 + num2;

    sub = num1 - num2;

    prod = num1 * num2;

    div = num1 / num2;

    printf("O valor a soma é:%i\n", soma);

    printf("O valor a subtração é:%i\n", sub);

    printf("O valor do produto é:%i\n", prod);

    printf("O valor da divisão é:%d\n", div);
    
    printf("\n");

    return 0;
}